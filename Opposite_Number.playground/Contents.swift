/*:
 
 # Opposite Number
 
Test problem from [codewars](https://www.codewars.com/kata/opposite-number)
 
Very simple, given a number, find its opposite.
 
 ```
1: -1
14: -14
-34: 34
...

 */
func opposite(number: Double) -> Double {
    return number * -1
}

opposite(number: 1)
opposite(number: 14)
opposite(number: -34)
